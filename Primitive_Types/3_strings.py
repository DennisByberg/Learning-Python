course = "Python Programming"

# 1. Get the length of the string
print(len(course))
# 2. Access the first character of the string
print(course[0])
# 3. Access the last character of the string
print(course[-1])
# 4. Slice the string from the beginning to the 3rd character (exclusive)
print(course[0:3])
# 5. Slice the string from the beginning to the 3rd character (exclusive) - shorthand
print(course[:3])
# 6. Slice the string from the beginning to the end
print(course[0:])
# 7. Slice the entire string
print(course[:])
